// Even or Odd:
//     Write a function that determines whether a given number is even or odd.
const isEvenOrOdd = (num) => {
    if (num % 2 === 0) {
        console.log("It's even!");
    } else {
        console.log("It's odd!");
    }
}

isEvenOrOdd(5);
isEvenOrOdd(-6);
isEvenOrOdd(4);
isEvenOrOdd(3);

